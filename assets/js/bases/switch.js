const dia = 1

switch(dia){//por defecto hace la comparacion con el ===
    case 0:
        console.log('Domingo')
        break;
    case 1:
        console.log('Lunes')
        break;
    case 2:
        console.log('Martes')
        break;
    default:
        console.log('Otro dia')
        break
}